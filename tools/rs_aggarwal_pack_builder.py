@dataclass(frozen=True)
class TextTopicSpec:
    chapter_number: int
    title_en: str
    title_hi: str
    concept_en: str
    concept_hi: str
    example_en: str
    example_hi: str
    prompt_en: str
    prompt_hi: str
    accepted_answers: tuple[str, ...]
    hint_en: str
    hint_hi: str
    support_en: str
    support_hi: str
    mistake_type: str = "GENERAL"


@dataclass(frozen=True)
class McqTopicSpec:
    chapter_number: int
    title_en: str
    title_hi: str
    concept_en: str
    concept_hi: str
    example_en: str
    example_hi: str
    prompt_en: str
    prompt_hi: str
    options: tuple[tuple[str, str], ...]
    correct_option_index: int
    hint_en: str
    hint_hi: str
    support_en: str
    support_hi: str
    mistake_type: str = "GENERAL"


TopicSpec = TextTopicSpec | McqTopicSpec


def loc(english: str, hindi: str | None = None) -> dict[str, str]:
    return {
        "english": english,
        "hindi": english if hindi is None else hindi,
    }


def slugify(value: str) -> str:
    return re.sub(r"[^a-z0-9]+", "_", value.lower()).strip("_")


def chapter_title(chapter_number: int) -> tuple[str, str]:
    return CHAPTERS[chapter_number]


def know_prompt(title_en: str, title_hi: str) -> dict[str, str]:
    return loc(
        f"Do you know this topic: {title_en}?",
        f"क्या आप यह विषय जानते हैं: {title_hi}?",
    )


def text_question(spec: TextTopicSpec) -> dict:
    return {
        "id": f"{slugify(spec.title_en)}_q1",
        "prompt": loc(spec.prompt_en, spec.prompt_hi),
        "type": "TEXT_INPUT",
        "options": [],
        "correctOptionIndex": None,
        "acceptedAnswers": list(spec.accepted_answers),
        "hint": None,
        "wrongReason": None,
        "supportExample": None,
        "mistakeType": spec.mistake_type,
        "reteachTitle": None,
        "reteachParagraphs": [],
    }


def mcq_question(spec: McqTopicSpec) -> dict:
    return {
        "id": f"{slugify(spec.title_en)}_q1",
        "prompt": loc(spec.prompt_en, spec.prompt_hi),
        "type": "MULTIPLE_CHOICE",
        "options": [loc(en, hi) for en, hi in spec.options],
        "correctOptionIndex": spec.correct_option_index,
        "acceptedAnswers": [],
        "hint": None,
        "wrongReason": None,
        "supportExample": None,
        "mistakeType": spec.mistake_type,
        "reteachTitle": None,
        "reteachParagraphs": [],
    }


def render_topic(spec: TopicSpec) -> dict:
    chapter_en, chapter_hi = chapter_title(spec.chapter_number)
    question = text_question(spec) if isinstance(spec, TextTopicSpec) else mcq_question(spec)
    slug = slugify(f"ch{spec.chapter_number:02d}_{spec.title_en}")
    return {
        "id": f"rs_{slug}",
        "sourceLessonId": f"rs_ch{spec.chapter_number:02d}",
        "chapterNumber": spec.chapter_number,
        "chapterTitle": loc(chapter_en, chapter_hi),
        "lessonTitle": loc(chapter_en, chapter_hi),
        "subtopicTitle": loc(spec.title_en, spec.title_hi),
        "knowPrompt": know_prompt(spec.title_en, spec.title_hi),
        "explanationTitle": loc(spec.title_en, spec.title_hi),
        "explanationParagraphs": [],
        "examples": [],
        "visuals": [],
        "questions": [question],
        "tags": [
            loc(chapter_en, chapter_hi),
            loc(spec.title_en, spec.title_hi),
        ],
        "mistakeFocus": spec.mistake_type,
    }


