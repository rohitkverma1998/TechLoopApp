def build_book() -> dict:
    from rs_aggarwal_exercise_builder import build_exercise_topics

    topics = [render_topic(spec) for spec in TOPIC_SPECS]
    topics.extend(build_exercise_topics(CHAPTERS))
    present = {topic["chapterNumber"] for topic in topics}
    missing = sorted(set(CHAPTERS) - present)
    if missing:
        raise ValueError(f"Missing chapters in generated pack: {missing}")
    return {
        "id": BOOK_ID,
        "subjectTitle": loc("Mathematics", "गणित"),
        "bookTitle": loc(BOOK_TITLE_EN, BOOK_TITLE_HI),
        "teacherNote": loc(
            "Use the main path first, then revision and weak-topic practice. This offline pack keeps the textbook flow chapter by chapter with many smaller topics.",
            "पहले मुख्य पथ चलाइए, फिर पुनरावृत्ति और कमज़ोर-विषय अभ्यास कराइए। यह ऑफलाइन पैक पुस्तक के अध्यायवार प्रवाह को छोटे-छोटे विषयों में रखता है।",
        ),
        "topics": topics,
    }


def write_pack(output_path: Path = OUTPUT_PATH) -> dict:
    book = build_book()
    save_book(output_path, book)
    return book


def main() -> None:
    book = write_pack()
    topics = book["topics"]
    chapter_counts: dict[int, int] = {}
    for topic in topics:
        chapter_counts[topic["chapterNumber"]] = chapter_counts.get(topic["chapterNumber"], 0) + 1
    print(f"Wrote {len(topics)} topics across {len(chapter_counts)} chapter files using {OUTPUT_PATH}")
    for chapter_number in sorted(chapter_counts):
        chapter_en, _ = chapter_title(chapter_number)
        print(f"Chapter {chapter_number:02d}: {chapter_counts[chapter_number]} topic(s) - {chapter_en}")


if __name__ == "__main__":
    main()
